"""
Perulangan sederhana dengan For
"""
print ("Ibu menyuruh membaca semua buku")
jumlah_buku=10
jumlah_buku_sudah_dibaca=0
for jumlah_buku_sudah_dibaca in range(1, jumlah_buku+1):
    print(f'buku ke # {jumlah_buku_sudah_dibaca} sudah dibaca')

print(f'\njumlah buku yang sudah dibaca {jumlah_buku_sudah_dibaca} buku')


